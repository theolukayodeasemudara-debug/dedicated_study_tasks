public class TaskThree{
    public static void main(String[] args){
        // I used a loop to print even numbers from 2 to 20.
        // The index starts from 2 and increases by 2 each time,
        // so only even numbers are printed.
        for(int index = 2; index <= 20; index+=2){
            System.out.println(index);
        }
    }
}
