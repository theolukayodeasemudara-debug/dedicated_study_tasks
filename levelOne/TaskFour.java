public class TaskFour{
    public static void main(String[] args){
        // I used a loop to print multiples of 3 from 3 to 20.
        // The index starts from 3 and keeps increasing by 3,
        // which makes every number printed a multiple of 3.
        for(int index = 3; index <= 20; index+=3){
            System.out.println(index);
        }
    }
}
