public class TaskTen{
    public static void main(String[] args){
        // I used a loop to go from 1 to 10.
        // For each number, I multiplied it by itself to get the square.
        // Then I printed each result on the same line separated by spaces.
        for(int index = 1; index <= 10; index++){
            System.out.print(index * index + " ");
        }
    }
}
