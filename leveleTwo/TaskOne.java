public class TaskOne{
// I used a while loop to print numbers from 1 to 20.
// The index starts at 1, and as long as it is less than or equal to 20,
// the loop keeps running. After each print, the index increases by 1
// so the loop can eventually stop.
    public static void main(String[] args){
         int index = 1;
         while(index <= 20){
             System.out.print(index + " ");
             index++;
         }
    }
}
